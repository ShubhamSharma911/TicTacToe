package model;

public abstract  class Player {
    protected String name;
    protected char symbol;

    abstract void makeMove();

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public char getSymbol() {
        return symbol;
    }

    public void setSymbol(char symbol) {
        this.symbol = symbol;
    }
}
